package com.flipkart.bean;

// Class representing an Admin
public class Admin {
    
    // Private member variable to store admin ID
    private int adminId;
    
    // Private member variable to store password
    private String password;
    
    // Getter method for retrieving admin ID
    public int getAdminId() {
        return adminId;
    }
    
    // Setter method for setting admin ID
    public void setAdminId(int adminId) {
        this.adminId = adminId;
    }
    
    // Getter method for retrieving password
    public String getPassword() {
        return password;
    }
    
    // Setter method for setting password
    public void setPassword(String password) {
        this.password = password;
    }
    
} // end of Admin class

